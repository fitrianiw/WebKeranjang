package com.fitri;

public class FormPendaftaranBean {
    private String namadepan;
    private String namabelakang;
    private String username;
    private String password;
    private String tempatlahir;
    private String email;
    private String alamat;


    /**
     * @return the namadepan
     */
    public String getNamadepan() {
        return namadepan;
    }

    /**
     * @param namadepan the namadepan to set
     */
    public void setNamadepan(String namadepan) {
        this.namadepan = namadepan;
    }

    /**
     * @return the namabelakang
     */
    public String getNamabelakang() {
        return namabelakang;
    }

    /**
     * @param namabelakang the namabelakang to set
     */
    public void setNamabelakang(String namabelakang) {
        this.namabelakang = namabelakang;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the tempatlahir
     */
    public String getTempatlahir() {
        return tempatlahir;
    }

    /**
     * @param tempatlahir the tempatlahir to set
     */
    public void setTempatlahir(String tempatlahir) {
        this.tempatlahir = tempatlahir;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the alamat
     */
    public String getAlamat() {
        return alamat;
    }

    /**
     * @param alamat the alamat to set
     */
    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
